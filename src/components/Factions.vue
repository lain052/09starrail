const selectedFaction = ref(null)

const currentFaction = computed(() => {
    if (route.params.faction) {
        return factions.value.find(f => f.route === route.params.faction)
    }
    return selectedFaction.value
})

// 计算相同命途的派系
const relatedFactions = computed(() => {
    if (!currentFaction.value) return []
    return factions.value.filter(faction => faction.path === currentFaction.value.path)
})

// 计算相关星神
const relatedGods = computed(() => {
    if (!currentFaction.value) return []
    return allStargods.filter(god => god.path === currentFaction.value.path)
})

const selectFaction = (faction) => {
    // 更新选中状态
    selectedFaction.value = selectedFaction.value?.id === faction.id ? null : faction
    // 进行路由跳转
    router.push(`/factions/${faction.route}`)
}

// 添加跳转到星神详情的函数
const goToStargod = (god) => {
    router.push(`/stargods/${god.route}`)
}

// 添加跳转到派系详情的函数
const goToFactionDetail = (faction) => {
    router.push(`/factions/${faction.route}`)
}

// 监听路由变化
watch(
    () => route.params.faction,
    (newFaction) => {
        if (newFaction) {
            selectedFaction.value = factions.value.find(f => f.route === newFaction) || null
        }
    },
    { immediate: true }
)
